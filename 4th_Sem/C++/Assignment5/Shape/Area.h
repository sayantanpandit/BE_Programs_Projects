
class Shape
{
	public :
	    void readData();
	    double Area();
};

class Rectangle : public Shape
{
      private :
	     double length;
             double breadth;
      public :
	    void readData();
	    double Area();
};

class Triangle : public Shape
{
        private :
         	double base;
                double height;
	public:
		void readData();
		double Area();
};

class Circle : public Shape
{
        private :
	     double radius;
	public :
		void readData();
		double Area();
};

